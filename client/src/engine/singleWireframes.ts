/** 단일 와이어프레임 % 블록 → §3 요소 슬라이드 (회색 존 골격, 즉시 편집 가능) */
export function wireframeToSlide(wf: SingleWireframe, aspect: DeckAspect = "16:9"): Slide {
  const { w: W, h: H } = aspectDims(aspect);
  const ZONE_FILL = "#F3F3F0";
  const ZONE_STROKE = "#D2D2CD";
  const els: SlideElement[] = wf.blocks.map((b) => {
    const [px, py, pw, ph, t] = b;
    const x = (px / 100) * W;
    const y = (py / 100) * H;
    const w = (pw / 100) * W;
    const h = (ph / 100) * H;
    if (t === 1) {
      // 강조/제목 바 — 얇으면 액센트 바, 두꺼우면 진한 헤더 존
      const thin = ph <= 7;
      return {
        id: uid(),
        type: "shape" as const,
        shape: "rect" as const,
        x, y, w, h,
        fill: thin ? "@accent" : "@textPrimary",
        opacity: thin ? 1 : 0.16,
      };
    }
    if (t === 2) {
      return {
        id: uid(),
        type: "shape" as const,
        shape: "ellipse" as const,
        x, y, w, h,
        fill: ZONE_FILL,
        stroke: ZONE_STROKE,
        strokeWidth: 2,
      };
    }
    return {
      id: uid(),
      type: "shape" as const,
      shape: "rect" as const,
      x, y, w, h,
      fill: ZONE_FILL,
      stroke: ZONE_STROKE,
      strokeWidth: 2,
    };
  });
  return { id: uid(), layout: "title-bullets", elements: els, notes: `[${wf.name}] 와이어프레임 — 회색 존을 실제 콘텐츠로 채우세요.` };
}
